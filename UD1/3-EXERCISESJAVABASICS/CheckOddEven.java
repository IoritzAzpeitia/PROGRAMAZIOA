public class CheckOddEven { 
   public static void main(String[] args) {  
      int number = 0;  
      System.out.println("The mark is " + number);
 
      // if-else statement
      if ( number % 2 == 0 ) {
         System.out.println( "Even Number" );
      } else {
         System.out.println( "Odd Number" );
      }
      System.out.println( "Done" );
   }
}